    fn install_test_input_labels(
        gar: &mut AuthTensorGen,
        ev: &mut AuthTensorEval,
        x: usize,
        y: usize,
    ) {
        let mut rng = ChaCha12Rng::seed_from_u64(0xDEAD_BEEF);
        let labels = gar.prepare_input_labels(
            &mut rng, x, y,
            &ev.alpha_auth_bit_shares, &ev.beta_auth_bit_shares,
        );
        ev.install_input_labels(labels);
    }

