/// Output of `AuthTensorGen::prepare_input_labels`, handed off to
/// `AuthTensorEval::install_input_labels` on matching wire indices.
///
/// Per `.planning/LABELS-BUG-CONTEXT.md` "Correct Target Architecture"
/// (BUG-02), input wire labels are an artifact of garbling — generated by
/// the garbler at garble time after the d-opening, not propagated through
/// preprocessing. This struct carries the eval-side halves of the input
/// sharings, the cleartext masked bits required for GGM-tree traversal,
/// and the LSB-shift blocks that align eval's wire-label LSBs with the
/// GGM-tree convention.
#[derive(Clone, Debug)]
pub struct InputLabelsForEval {
    /// Eval's halves of (sharing of x_i under δ_a). Length n.
    /// Each entry is the `key` half of an auth-bit-style sharing of
    /// gen's input bit x_i; gen retains the `mac` half locally in
    /// `AuthTensorGen.x_gen`. Pair encodes `x_i · δ_a`.
    pub x_input_eval_keys: Vec<Block>,
    /// Eval's halves of (sharing of y_j under δ_a). Length m.
    pub y_input_eval_keys: Vec<Block>,
    /// Cleartext masked bits d_x[i] = x_i XOR α_i. Length n.
    /// Used by eval as choice bits for first-half GGM-tree traversal,
    /// replacing the prior LSB-of-wire-label readout.
    pub masked_x_bits: Vec<bool>,
    /// Cleartext masked bits d_y[j] = y_j XOR β_j. Length m.
    /// Used for second-half GGM-tree traversal.
    pub masked_y_bits: Vec<bool>,
    /// LSB-shift blocks for x: `(x_i XOR a_i) · δ_a` per i. Length n.
    /// Eval XORs this into its `masked_x_gen[i]` during install. Without
    /// the shift, eval's wire-label LSB would be `b_i` (eval's local
    /// α-share); with it, the LSB becomes `d_i` — matching the GGM-tree
    /// convention that `gen_populate_seeds_mem_optimized` requires
    /// (gen's seed LSB=0, eval's seed LSB=d). Combined gen+eval sum is
    /// unchanged because gen has already XOR'd the same shift into its
    /// own `masked_x_gen` before this struct is returned.
    pub x_lsb_shifts: Vec<Block>,
    /// LSB-shift blocks for y. Length m. See `x_lsb_shifts`.
    pub y_lsb_shifts: Vec<Block>,
}

pub struct AuthTensorGen {
    cipher: &'static FixedKeyAes,
    chunking_factor: usize,

    n: usize,
    m: usize,

    pub delta_a: Delta,

    /// Underlying auth-bit triples + lowered Block-form sharings; see
    /// `TensorFpreGen` field doc for semantics. The `_eval` and `_gen` Block
    /// fields are local-only lowerings, populated by preprocessing and
    /// consumed by online code without further interaction.
    pub alpha_auth_bit_shares: Vec<AuthBitShare>,
    pub alpha_eval: Vec<Block>,
    pub alpha_gen:  Vec<Block>,
    pub beta_auth_bit_shares: Vec<AuthBitShare>,
    pub beta_eval: Vec<Block>,
    pub beta_gen:  Vec<Block>,
    pub correlated_auth_bit_shares: Vec<AuthBitShare>,
    pub correlated_eval: Vec<Block>,
    pub correlated_gen:  Vec<Block>,
    pub gamma_auth_bit_shares: Vec<AuthBitShare>,
    pub gamma_eval: Vec<Block>,
    pub gamma_gen:  Vec<Block>,

    /// Gen's half of (sharing of x under δ_a). Length n. Populated by
    /// `prepare_input_labels` (BUG-02 / Phase 1.2). Auth-bit-style:
    /// gen retains the `mac` half here; eval receives the `key` half via
    /// `InputLabelsForEval.x_input_eval_keys`. Pair encodes `x_i · δ_a`.
    pub x_gen: Vec<Block>,
    /// Gen's half of (sharing of y under δ_a). Length m.
    pub y_gen: Vec<Block>,
    /// Gen's half of (sharing of x XOR α under δ_a) — the input wire
    /// label, gen side. Length n. Populated by `prepare_input_labels`.
    /// Equals `x_gen[i] XOR (key_g_α XOR a_i · δ_a)`. Used as the
    /// GGM-tree seed input by `garble_first_half` once callers migrate
    /// (Phase 1.2(b)).
    pub masked_x_gen: Vec<Block>,
    /// Gen's half of (sharing of y XOR β under δ_a). Length m.
    pub masked_y_gen: Vec<Block>,

    pub first_half_out: BlockMatrix,
    pub second_half_out: BlockMatrix,

    /// D_ev (rho-half) accumulator for the first half-outer-product. Phase 9 P2-02.
    /// Mirrors `first_half_out` but accumulates the rho-half PRG outputs from
    /// `gen_unary_outer_product_wide`. Written by `garble_first_half_p2` /
    /// `garble_second_half_p2`; consumed by `garble_final_p2`.
    pub first_half_out_ev: BlockMatrix,
    /// D_ev (rho-half) accumulator for the second half-outer-product. Phase 9 P2-02.
    pub second_half_out_ev: BlockMatrix,

    /// Set to `true` by `garble_final()`. `compute_lambda_gamma()` asserts
    /// this flag to prevent silent garbage output when called out of order.
    final_computed: bool,
}

